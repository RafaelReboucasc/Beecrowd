#include <stdio.h>
#include <string.h>

int main()
{
    int n;
    scanf("%d", &n);
    char numeros[20];
    
        for(int i = 0; i < n; i++){
            scanf("%s", &numeros);
            if(strlen(numeros)==5)
                printf("3\n");
            if((numeros[0] == 't' && numeros[1] == 'w') || (numeros[0] == 't' && numeros[2] == 'o') || (numeros[1] == 'w' && numeros[2] == 'o'))
                printf("2\n");
            if((numeros[0] == 'o' && numeros[1] == 'n') || (numeros[0] == 'o' && numeros[2] == 'e') || (numeros[1] == 'n' && numeros[2] == 'e'))
                printf("1\n");
          
        }
    

    return 0;
}
